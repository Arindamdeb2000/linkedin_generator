const getYourNameMaybe = (name) => {
  if (name.length < 2) {
    return ''
  }
  const rand = Math.random()
  // Probably return your name
  if (rand >= 0 && rand < 0.5) {
    return toTitleCase(name)
  }
  // Perhaps return a name like your name
  else if (rand >= 0.50 && rand < 0.99) {
    let diffThreshold = 1 - getRandomArbitrary(0.01, 0.4)
    let maxSim = -1
    let currentWinner = name
    let sim;
    for (let i = 0; i < NAMES.length; i++) {
      if (NAMES[i].charAt(0).toUpperCase() !== name.charAt(0).toUpperCase()) {
        continue
      }
      sim = similarity(name, NAMES[i])
      if (sim <= diffThreshold && sim > maxSim) {
        maxSim = sim
        currentWinner = NAMES[i]
      }
    }
    return toTitleCase(currentWinner)
  }
  else {
    // Otherwise you're probably named Matt
    return 'Matt'
  }
}
